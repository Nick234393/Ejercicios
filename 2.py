Dia = int(input("Ingrese numero del 1 al 7, que represente los dias de la semana "))

if Dia==1:
    print("Es Lunes")
elif Dia==2:
    print("Es Martes")
elif Dia==3:
    print("Es Miercoles")
elif Dia==4:
    print("Feliz Jueves")
elif Dia==5:
    print("Es Viernes")
elif Dia==6:
    print("Es Sabado")
elif Dia==7:
    print("Es Domingo")
else:
    print("No existe dia")